/**
 * Parse a sidebars.js file content using regex extraction.
 *
 * Handles both ESM and CJS formats. Extracts the sidebar object and
 * recursively parses categories, docs, and string entries.
 */
export function parseSidebarsConfig(content: string): SidebarsConfig {
  // Try to extract the sidebars object as JSON-like content.
  // Strip comments first.
  let cleaned = content
    .replace(/\/\/.*$/gm, '')
    .replace(/\/\*[\s\S]*?\*\//g, '');

  // Find the main export (ESM or CJS)
  // export default { ... } or module.exports = { ... }
  const exportMatch = cleaned.match(
    /(?:export\s+default|module\.exports\s*=)\s*(\{[\s\S]*\})\s*;?\s*$/,
  );

  if (!exportMatch) return {};

  let objStr = exportMatch[1];

  // Convert JS object literal to valid JSON:
  // - Add quotes around unquoted keys
  // - Replace single quotes with double quotes
  // - Remove trailing commas
  objStr = objStr
    .replace(/(\w+)\s*:/g, '"$1":')
    .replace(/'/g, '"')
    .replace(/,\s*([}\]])/g, '$1');

  try {
    return JSON.parse(objStr) as SidebarsConfig;
  } catch {
    // If JSON parse fails, return empty – the content is too complex for
    // regex-based parsing (e.g. uses JS functions, requires, etc.)
    return {};
  }
}

/**
 * Convert parsed Docusaurus sidebar items into Tome NavigationGroup format.
 */
export function convertSidebarItems(
  items: SidebarItem[],
  docsDir?: string,
): Array<string | NavigationGroup> {
  const result: Array<string | NavigationGroup> = [];

  for (const item of items) {
    if (typeof item === 'string') {
      // Simple string reference – use as-is
      result.push(item);
    } else if (item.type === 'doc') {
      result.push(item.id);
    } else if (item.type === 'category') {
      const group: NavigationGroup = {
        group: item.label,
        pages: convertSidebarItems(item.items, docsDir),
      };
      result.push(group);
    } else if (item.type === 'autogenerated') {
      // For autogenerated sidebars, scan the directory if provided
      if (docsDir) {
        const dirPath = item.dirName === '.'
          ? docsDir
          : join(docsDir, item.dirName);
        if (existsSync(dirPath)) {
          const files = scanDocsDir(dirPath);
          for (const f of files) {
            const rel = relative(docsDir, f);
            const page = rel.replace(/\.(mdx?|md)$/, '');
            result.push(page);
          }
        }
      }
    }
    // type: 'link' items are external links; skip them in navigation
  }

  return result;
}

/**
 * Convert a full SidebarsConfig into Tome NavigationGroup array.
 */
export function convertDocusaurusSidebars(
  sidebars: SidebarsConfig,
  docsDir?: string,
): NavigationGroup[] {
  const groups: NavigationGroup[] = [];

  for (const [sidebarName, items] of Object.entries(sidebars)) {
    if (!Array.isArray(items)) continue;

    const converted = convertSidebarItems(items, docsDir);

    // If items are all top-level categories, flatten them as separate groups
    const allCategories = converted.every(
      (c) => typeof c !== 'string' && 'group' in c,
    );

    if (allCategories && converted.length > 0) {
      for (const c of converted) {
        if (typeof c !== 'string' && 'group' in c) {
          groups.push(c as NavigationGroup);
        }
      }
    } else {
      // Use the sidebar name as the group name
      groups.push({
        group: sidebarName.charAt(0).toUpperCase() + sidebarName.slice(1),
        pages: converted,
      });
    }
  }

  return groups;
}

