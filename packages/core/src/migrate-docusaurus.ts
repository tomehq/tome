/**
 * Docusaurus-to-Tome migration converter.
 *
 * Reads a Docusaurus project (docusaurus.config.js + sidebars.js + docs/),
 * converts components and configuration into Tome equivalents, and writes
 * the result to an output directory.
 */

import {
  readFileSync,
  existsSync,
  writeFileSync,
  mkdirSync,
  copyFileSync,
  readdirSync,
  statSync,
} from 'fs';
import { join, relative, dirname, extname, basename } from 'path';
import matter from 'gray-matter';

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export type NavigationGroup = {
  group: string;
  pages: Array<string | NavigationGroup>;
};

export type MigrationResult = {
  pages: number;
  redirects: number;
  warnings: string[];
  convertedFiles: string[];
};

export type DocusaurusConfig = {
  title?: string;
  tagline?: string;
  url?: string;
  baseUrl?: string;
  favicon?: string;
  themeConfig?: {
    navbar?: {
      title?: string;
      logo?: { alt?: string; src?: string };
      items?: Array<{ label?: string; href?: string; to?: string; position?: string }>;
    };
    footer?: {
      style?: string;
      links?: Array<{ title?: string; items?: Array<{ label?: string; href?: string; to?: string }> }>;
    };
    colorMode?: {
      defaultMode?: string;
      disableSwitch?: boolean;
    };
  };
};

export type SidebarItem =
  | string
  | { type: 'doc'; id: string; label?: string }
  | { type: 'category'; label: string; items: SidebarItem[] }
  | { type: 'autogenerated'; dirName: string }
  | { type: 'link'; label: string; href: string };

export type SidebarsConfig = Record<string, SidebarItem[]>;

// ---------------------------------------------------------------------------
// Config parsing
// ---------------------------------------------------------------------------

/**
 * Parse a docusaurus.config.js file content using regex extraction.
 *
 * Handles both ESM (`export default { ... }`) and CJS (`module.exports = { ... }`)
 * formats. Uses regex since we cannot safely eval arbitrary config files.
 */
export function parseDocusaurusConfig(content: string): DocusaurusConfig {
  const config: DocusaurusConfig = {};

  // Extract title
  const titleMatch = content.match(/title\s*:\s*['"`]([^'"`]+)['"`]/);
  if (titleMatch) config.title = titleMatch[1];

  // Extract tagline
  const taglineMatch = content.match(/tagline\s*:\s*['"`]([^'"`]+)['"`]/);
  if (taglineMatch) config.tagline = taglineMatch[1];

  // Extract url
  const urlMatch = content.match(/\burl\s*:\s*['"`]([^'"`]+)['"`]/);
  if (urlMatch) config.url = urlMatch[1];

  // Extract baseUrl
  const baseUrlMatch = content.match(/baseUrl\s*:\s*['"`]([^'"`]+)['"`]/);
  if (baseUrlMatch) config.baseUrl = baseUrlMatch[1];

  // Extract favicon
  const faviconMatch = content.match(/favicon\s*:\s*['"`]([^'"`]+)['"`]/);
  if (faviconMatch) config.favicon = faviconMatch[1];

  // Extract navbar title from themeConfig
  const navbarTitleMatch = content.match(
    /navbar\s*:\s*\{[^}]*title\s*:\s*['"`]([^'"`]+)['"`]/s,
  );
  if (navbarTitleMatch) {
    config.themeConfig = config.themeConfig || {};
    config.themeConfig.navbar = config.themeConfig.navbar || {};
    config.themeConfig.navbar.title = navbarTitleMatch[1];
  }

  // Extract navbar logo src
  const logoSrcMatch = content.match(
    /logo\s*:\s*\{[^}]*src\s*:\s*['"`]([^'"`]+)['"`]/s,
  );
  if (logoSrcMatch) {
    config.themeConfig = config.themeConfig || {};
    config.themeConfig.navbar = config.themeConfig.navbar || {};
    config.themeConfig.navbar.logo = { src: logoSrcMatch[1] };
  }

  // Extract colorMode defaultMode
  const colorModeMatch = content.match(
    /colorMode\s*:\s*\{[^}]*defaultMode\s*:\s*['"`]([^'"`]+)['"`]/s,
  );
  if (colorModeMatch) {
    config.themeConfig = config.themeConfig || {};
    config.themeConfig.colorMode = { defaultMode: colorModeMatch[1] };
  }

  return config;
}

