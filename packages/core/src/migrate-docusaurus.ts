/**
 * Migrate a Docusaurus documentation project to Tome.
 *
 * @param sourceDir  Root of the Docusaurus project (where docusaurus.config.js lives).
 * @param outDir     Destination directory for the Tome project.
 * @param options    Optional flags - `dryRun` skips writing to disk.
 */
export async function migrateFromDocusaurus(
  sourceDir: string,
  outDir: string,
  options?: { dryRun?: boolean },
): Promise<MigrationResult> {
  const warnings: string[] = [];
  const convertedFiles: string[] = [];
  const dryRun = options?.dryRun ?? false;

  // 1. Read and parse docusaurus.config.js or docusaurus.config.ts --------
  const configJsPath = join(sourceDir, 'docusaurus.config.js');
  const configTsPath = join(sourceDir, 'docusaurus.config.ts');
  const configPath = existsSync(configTsPath) ? configTsPath : configJsPath;

  let docConfig: DocusaurusConfig = {};
  if (existsSync(configPath)) {
    const configContent = readFileSync(configPath, 'utf-8');
    docConfig = parseDocusaurusConfig(configContent);
  } else {
    warnings.push('No docusaurus.config.js or docusaurus.config.ts found');
  }

  // 2. Read and parse sidebars.js -----------------------------------------
  const sidebarsJsPath = join(sourceDir, 'sidebars.js');
  const sidebarsJsonPath = join(sourceDir, 'sidebars.json');
  const sidebarsPath = existsSync(sidebarsJsPath)
    ? sidebarsJsPath
    : existsSync(sidebarsJsonPath)
      ? sidebarsJsonPath
      : null;

  let sidebars: SidebarsConfig = {};
  const docsDir = join(sourceDir, 'docs');

  if (sidebarsPath) {
    const sidebarsContent = readFileSync(sidebarsPath, 'utf-8');
    if (sidebarsPath.endsWith('.json')) {
      try {
        sidebars = JSON.parse(sidebarsContent) as SidebarsConfig;
      } catch {
        warnings.push('Failed to parse sidebars.json');
      }
    } else {
      sidebars = parseSidebarsConfig(sidebarsContent);
    }
  }

  // 3. Convert config -----------------------------------------------------
  const tomeConfig = convertDocusaurusConfig(docConfig, sidebars, docsDir);

  // 4. Collect and convert content files (.mdx / .md) ---------------------
  const contentDir = existsSync(docsDir) ? docsDir : sourceDir;
  const allFiles = walkDir(contentDir);
  const contentFiles = allFiles.filter((f) => {
    const ext = extname(f).toLowerCase();
    return ext === '.mdx' || ext === '.md';
  });

  const pagesDir = join(outDir, 'pages');

  for (const filePath of contentFiles) {
    const raw = readFileSync(filePath, 'utf-8');
    const rel = relative(contentDir, filePath);

    // Preserve frontmatter
    const { data: frontmatter, content: body } = matter(raw);

    // Convert Docusaurus-specific frontmatter
    if (frontmatter.sidebar_label && !frontmatter.title) {
      frontmatter.title = frontmatter.sidebar_label;
    }
    // Clean up Docusaurus-specific frontmatter keys
    delete frontmatter.sidebar_label;
    delete frontmatter.sidebar_position;
    delete frontmatter.slug;
    delete frontmatter.custom_edit_url;

    const { converted, hasJsx } = convertDocusaurusContent(body);

    // Check for components that need manual attention
    if (/<BrowserWindow[\s>]/i.test(converted)) {
      warnings.push(
        `${rel}: contains <BrowserWindow> which may need manual conversion`,
      );
    }
    if (/<DocCardList[\s>]/i.test(converted)) {
      warnings.push(
        `${rel}: contains <DocCardList> which may need manual conversion`,
      );
    }

    // Reconstruct file with frontmatter
    const outContent = matter.stringify(converted, frontmatter);

    // Determine output extension
    const outFileName = hasJsx
      ? rel.replace(/\.md$/, '.mdx')
      : rel;
    const outPath = join(pagesDir, outFileName);

    if (!dryRun) {
      mkdirSync(dirname(outPath), { recursive: true });
      writeFileSync(outPath, outContent, 'utf-8');
    }

    convertedFiles.push(outFileName);
  }

  // 5. Copy static assets -------------------------------------------------
  if (!dryRun) {
    const publicDir = join(outDir, 'public');

    // Docusaurus static assets live in static/
    const staticDir = join(sourceDir, 'static');
    if (existsSync(staticDir)) {
      copyDirRecursive(staticDir, join(publicDir, 'static'));
    }
  }

  // 6. Generate tome.config.js --------------------------------------------
  if (!dryRun) {
    mkdirSync(outDir, { recursive: true });

    const configContent = [
      '/** @type {import("@tomehq/core").TomeConfig} */',
      `export default ${JSON.stringify(tomeConfig, null, 2)};`,
      '',
    ].join('\n');

    writeFileSync(join(outDir, 'tome.config.js'), configContent, 'utf-8');
  }

  // 7. Build result -------------------------------------------------------
  return {
    pages: convertedFiles.length,
    redirects: 0,
    warnings,
    convertedFiles,
  };
}
