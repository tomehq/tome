/**
 * Convert Docusaurus-specific MDX syntax to Tome equivalents.
 *
 * Returns the converted string and a flag indicating whether the content
 * contains JSX (i.e. should be written as .mdx).
 */
export function convertDocusaurusContent(content: string): {
  converted: string;
  hasJsx: boolean;
} {
  let result = content;
  let hasJsx = false;

  // 1. Strip Docusaurus component imports ----------------------------------
  //    e.g. import Tabs from '@theme/Tabs';
  //         import TabItem from '@theme/TabItem';
  //         import anything from '@site/...'
  // Strip Docusaurus imports line by line to avoid regex backtracking
  result = result.split('\n').map(line => {
    const trimmed = line.trim();
    if (trimmed.startsWith('import ') && /from\s+['"]@(theme|site|docusaurus)\//.test(trimmed)) {
      return '';
    }
    return line;
  }).join('\n');

  // Also strip require-style imports
  result = result.split('\n').map(line => {
    const trimmed = line.trim();
    if (trimmed.startsWith('const ') && /require\(\s*['"]@(theme|site|docusaurus)\//.test(trimmed)) {
      return '';
    }
    return line;
  }).join('\n');

  // 2. Admonitions ---------------------------------------------------------
  //    :::note     → <Callout type="info">
  //    :::tip      → <Callout type="tip">
  //    :::info     → <Callout type="info">
  //    :::warning  → <Callout type="warning">
  //    :::danger   → <Callout type="error">
  //    :::caution  → <Callout type="warning">
  //    Optional title: :::note My Title → <Callout type="info" title="My Title">
  const admonitionMap: Record<string, string> = {
    note: 'info',
    tip: 'tip',
    info: 'info',
    warning: 'warning',
    danger: 'error',
    caution: 'warning',
  };

  // Match admonition blocks: :::type optional title\ncontent\n:::
  // The title must be on the same line as :::type (use [^\n] not . to avoid
  // matching across the newline).
  const admonitionRe = /^:::(note|tip|info|warning|danger|caution)(?:[^\S\n]+([^\n]+))?\n([\s\S]*?)^:::\s*$/gm;
  result = result.replace(admonitionRe, (_match, type: string, title: string, body: string) => {
    const calloutType = admonitionMap[type] || 'info';
    const trimmedBody = body.trimEnd();
    hasJsx = true;

    if (title && title.trim()) {
      return `<Callout type="${calloutType}" title="${title.trim()}">\n${trimmedBody}\n</Callout>`;
    }
    return `<Callout type="${calloutType}">\n${trimmedBody}\n</Callout>`;
  });

  // 3. Tabs / TabItem → Tome Tabs / Tab -----------------------------------
  //    <Tabs> stays the same (or add items prop)
  //    <TabItem value="x" label="Y">…</TabItem> → <Tab>…</Tab>

  // Extract tab labels and convert
  {
    const tabsStartTag = '<Tabs';
    let searchIdx = 0;

    while (true) {
      const startIdx = result.indexOf(tabsStartTag, searchIdx);
      if (startIdx === -1) break;

      // Find the matching </Tabs>
      const endTag = '</Tabs>';
      const endIdx = result.indexOf(endTag, startIdx);
      if (endIdx === -1) break;

      const fullBlock = result.slice(startIdx, endIdx + endTag.length);

      // Extract TabItem labels
      const labelRe = /<TabItem[^>]*label=["']([^"']+)["'][^>]*>/g;
      const labels: string[] = [];
      let labelMatch: RegExpExecArray | null;

      while ((labelMatch = labelRe.exec(fullBlock)) !== null) {
        labels.push(labelMatch[1]);
      }

      // Replace <TabItem ...> with <Tab> and </TabItem> with </Tab>
      let converted = fullBlock;
      converted = converted.replace(/<TabItem[^>]*>/g, '<Tab>');
      converted = converted.replace(/<\/TabItem>/g, '</Tab>');

      // Replace opening <Tabs...> with <Tabs items={[...]}>
      if (labels.length > 0) {
        const itemsStr = JSON.stringify(labels);
        converted = converted.replace(
          /<Tabs[^>]*>/,
          `<Tabs items={${itemsStr}}>`,
        );
      } else {
        converted = converted.replace(/<Tabs[^>]*>/, '<Tabs>');
      }

      result = result.slice(0, startIdx) + converted + result.slice(endIdx + endTag.length);
      searchIdx = startIdx + converted.length;
      hasJsx = true;
    }
  }

  // 4. Code blocks with title attribute -----------------------------------
  //    ```js title="example.js"  → ```js example.js
  result = result.replace(
    /```(\w+)\s+title=["']([^"']+)["']/g,
    '```$1 $2',
  );

  // 5. Strip @site/ path references in images/links ----------------------
  result = result.replace(/@site\//g, '');

  // 6. Check for remaining JSX if we haven't flagged it yet
  if (!hasJsx) {
    hasJsx = /<[A-Z][\w.]*[\s>]/.test(result);
  }

  // Clean up multiple consecutive blank lines from stripped imports
  result = result.replace(/\n{3,}/g, '\n\n');

  return { converted: result, hasJsx };
}

