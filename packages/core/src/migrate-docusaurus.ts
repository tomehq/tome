/** Convert a parsed DocusaurusConfig into a Tome-compatible config object. */
export function convertDocusaurusConfig(
  docConfig: DocusaurusConfig,
  sidebars?: SidebarsConfig,
  docsDir?: string,
): Record<string, any> {
  const tome: Record<string, any> = {};

  // Name – prefer navbar title, fall back to site title
  const name = docConfig.themeConfig?.navbar?.title || docConfig.title;
  if (name) tome.name = name;

  // Logo
  if (docConfig.themeConfig?.navbar?.logo?.src) {
    tome.logo = docConfig.themeConfig.navbar.logo.src;
  }

  // Favicon
  if (docConfig.favicon) {
    tome.favicon = docConfig.favicon;
  }

  // Top navigation items from navbar
  const navItems = docConfig.themeConfig?.navbar?.items;
  if (navItems && navItems.length > 0) {
    const topNav = navItems
      .filter((item) => item.label && (item.href || item.to))
      .map((item) => ({
        label: item.label!,
        href: item.href || item.to || '',
      }));

    if (topNav.length > 0) {
      tome.topNav = topNav;
    }
  }

  // Navigation from sidebars
  if (sidebars) {
    const navigation = convertDocusaurusSidebars(sidebars, docsDir);
    if (navigation.length > 0) {
      tome.navigation = navigation;
    }
  }

  return tome;
}

